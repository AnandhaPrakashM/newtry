package org.practice;

public class Encap {
	
	private int a;
	
	private int b;
	
	private String s;

	public int getA() {
		return a;
	}

	public void setA(int a) {
		this.a = a;
	}

	public int getB() {
		return b;
	}

	public void setB(int b) {
		this.b = b;
	}

	public String getS() {
		return s;
	}

	public void setS(String s) {
		this.s = s;
	}

	public Encap(int a, int b, String s) {
		super();
		this.a = a;
		this.b = b;
		this.s = s;
	}
	
	

}
